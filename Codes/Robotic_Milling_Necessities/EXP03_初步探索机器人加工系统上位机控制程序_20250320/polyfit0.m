% 定义数据
set_speed = [102, 195, 275, 367, 450, 540, 625, 710, 800, 888, 973, 1060, 1145, 1230, 1320];
actual_speed = [100, 200, 300, 400, 500, 600, 700, 800, 900, 1000, 1100, 1200, 1300, 1400, 1500];

% 使用 polyfit 进行一次线性拟合
p = polyfit(actual_speed,set_speed, 1);

% 输出拟合直线的斜率和截距
fprintf('拟合直线的斜率: %.4f\n', p(1));
fprintf('拟合直线的截距: %.4f\n', p(2));
